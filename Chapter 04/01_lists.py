# Create a list using []
a = [1,2,4,28,6]

# Print the list using print() function
print(a)

# Accessing using index using a[0], a[1], a[2]
print(a[2])

# Change the value of list using 
a[0] = 52
print(a)

# We can create a list with items of different types
c = [45,"Harry", False, (69)]
print(c)

# We can store value of any data type

# List slicing
friends = ["Abhishek", "Rohan", "Bikram", "Anita","Sunita","Sudeepa", "Priyanka"]
